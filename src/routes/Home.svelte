<section class="waarom">
  <div class="padded">
    <h2>Waarom een film over de Biesbosch?</h2>

    <p>
      Na het maken van onze vorige film, <a
        href="https://www.dordtfilm.nl"
        target="_blank">Dordrecht door de jaren heen</a
      >, hebben we onze passie ontdekt voor het maken van documentaires en zijn
      onze ogen geopend voor de prachtige natuur van de Biesbosch. De aangewezen
      persoon om ons te helpen dit in beeld te brengen is oud-boswachter Jacques
      van der Neut. Dankzij zijn tientallen jaren Biesbosch-ervaring en kennis
      weten we precies wat belangrijk is om te vertellen en te laten zien.
    </p>

    <p>
      De Biesbosch is een zoetwatergetijdengebied, dat is uniek op wereldschaal.
      Na honderden jaren gebruikt te zijn voor industriële doeleinden is het
      sinds 1994 een nationaal park geworden. Sindsdien is de natuur echt tot
      bloei gekomen. Als je in de Biesbosch bent voel je meteen de rust en ga je
      op in de natuur. Wij vinden dit een groot goed en laten mensen graag zien
      en voelen hoe mooi het is, zowel in de bioscoop als thuis.
    </p>
  </div>
